<template>
  <div class="search-form">
    <!-- 头部tab切换 -->
    <el-row type="flex" class="search-tab">
      <span
        v-for="(item, index) in tabs"
        :key="index"
        @click="handleSearchTab(item, index)"
        :class="{active: index === currentTab}"
      >
        <i :class="item.icon"></i>
        {{item.name}}
      </span>
    </el-row>

    <el-form class="search-form-content" ref="form" label-width="80px">
      <el-form-item label="出发城市">
        <!-- fetch-suggestions 返回输入建议的方法 -->
        <!-- select 点击选中建议项时触发 -->
        <el-autocomplete
          :fetch-suggestions="queryDepartSearch"
          placeholder="请搜索出发城市"
          @select="handleDepartSelect"
          class="el-autocomplete"
          v-model="form.departCity"
        ></el-autocomplete>
      </el-form-item>
      <el-form-item label="到达城市">
        <el-autocomplete
          :fetch-suggestions="queryDestSearch"
          placeholder="请搜索到达城市"
          @select="handleDestSelect"
          class="el-autocomplete"
          v-model="form.destCity"
        ></el-autocomplete>
      </el-form-item>
      <el-form-item label="出发时间">
        <!-- change 用户确认选择日期时触发 -->
        <el-date-picker
          type="date"
          placeholder="请选择日期"
          style="width: 100%;"
          v-model="form.departDate"
          @change="handleDate"
          :picker-options="pickerOptions1"
        ></el-date-picker>
      </el-form-item>
      <el-form-item label>
        <el-button style="width:100%;" type="primary" icon="el-icon-search" @click="handleSubmit">搜索</el-button>
      </el-form-item>
      <div class="reverse">
        <span @click="handleReverse">换</span>
      </div>
    </el-form>
  </div>
</template>

<script>
export default {
  data() {
    return {
      tabs: [
        { icon: "iconfont icondancheng", name: "单程" },
        { icon: "iconfont iconshuangxiang", name: "往返" }
      ],
      form: {
        departCity: "", // 出发城市
        departCode: "", // 出发城市代码
        destCity: "", // 到达城市
        destCode: "", // 到达城市代码
        departDate: "" // 日期字符串
      },
      pickerOptions1: {
        disabledDate(time) {
          return time.getTime() + 3600 * 1000 * 24< Date.now();
        }
      },
      currentTab: 0,
      restaurants: [],
      destData: [], //出发城市数组
      toData: [] //到达城市
    };
  },
  methods: {
    // tab切换时触发
    handleSearchTab(item, index) {
      this.currentTab = index;
    },

    // 出发城市输入框获得焦点时触发
    // value 是选中的值，cb是回调函数，接收要展示的列表
    queryDepartSearch(value, cb) {
      if (value.trim() === "") {
        cb([]);
        return;
      }
      this.$axios({
        url: "/airs/city",
        params: {
          name: value
        }
      }).then(res => {
        const { data } = res.data;
        this.destData = data.map(v => {
          v.value = v.name.replace("市", "");
          return v;
        });
        // console.log(data)
        cb(this.destData);
      });
    },

    // 目标城市输入框获得焦点时触发
    // value 是选中的值，cb是回调函数，接收要展示的列表
    queryDestSearch(value, cb) {
      if (value.trim() === "") {
        cb([]);
        return;
      }
      this.$axios({
        url: "/airs/city",
        params: {
          name: value
        }
      }).then(res => {
        console.log(res);
        const { data } = res.data;
        this.toData = data.map(v => {
          v.value = v.name.replace("市", "");
          return v;
        });
        // console.log(this.destData)
        cb(this.toData);
      });
    },

    // 出发城市下拉选择时触发
    handleDepartSelect(item) {
      console.log(item);
      this.form.departCity = item.value;
      this.form.departCode = item.sort;
    },

    // 目标城市下拉选择时触发
    handleDestSelect(item) {
      this.form.destCity = item.value;
      this.form.destCode = item.sort;
    },

    // 确认选择日期时触发
    handleDate(value) {
      let year = value.getFullYear(); //年
      let month = value.getMonth() + 1; //月
      let day = value.getDate(); //日
      if (month < 10) month = "0" + month;
      if (day < 10) day = "0" + day;
      this.form.departDate = year + "-" + month + "-" + day;
      console.log(year + "-" + month + "-" + day);
    },

    // 触发和目标城市切换时触发
    handleReverse() {},

    // 提交表单是触发
    handleSubmit() {
      const obj = {
        departCity:{
          val: this.form.departCity,
          err: '出发城市不能为空'
        },
        destCity:{
          val: this.form.destCity,
          err: '到达城市不能为空'
        },
        departDate:{
          val: this.form.departDate,
          err: '时间不能为空'
        }
      }
      let bl = true
      Object.keys(obj).some(item =>{
        if(bl === false){
          return
        }
        if(obj[item].val === ''){
          this.$message.error(obj[item].err)
          bl = false
          return
        }
      })
      if(bl === false){
        return
      }
      this.$router.push({
        path:'/air/flights',
        query:this.form
      })
    }
  },
  mounted() {}
};
</script>

